package Program1;

public class Edge {
    int edgeNumber;
    int x1;
    int y1;
    int x2;
    int y2;

    public Edge() {

    }

    public Edge(int edgeNumber, int x1, int y1, int x2, int y2) {
        this.edgeNumber = edgeNumber;
        this.x1 = x1;
        this.y1 = y1;
        this.x2 = x2;
        this.y2 = y2;
    }

}
